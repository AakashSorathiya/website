<div class="section">
	<h1>Terminations</h1>
	<ul>
		<li><p>If you need to terminate a large number of students to "clean out" your Kronos, contact us for an Oracle report.</p></li>
		<li><p>Once you have received the Oracle report, put a "T" next to the student you want to terminate.</p></li>
		<li><p>Include the end date you wish to terminate the students - use the Payroll Pay Period End Dates.</p></li>
		<li><p>Send the reports to us in the interdepartmental mail - do not fax the reports.</p></li>
		<li><p>If a terminated student had a student employee computer account through ITS, contact ITS to verify that the computer account has been terminated.</p></li>
	</ul>
