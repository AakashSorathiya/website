	<h2 id="5">5. On-Campus Employment Criteria</h2>

	<p>The RIT - SEO is committed to providing equal employment opportunities for all eligible RIT student applicants. Equal employment is provided on the basis of demonstrated abilities, experience, training and financial need. There will be no discrimination based on race, color, creed, sex, age, national origin, marital status, veteran status or handicap in the selection, utilization, promotion or termination of any student.</p>
	<p>In accordance with Section 86.9 of the Federal Regulations implementing Title IX of the Educational Amendments of 1982, RIT does not discriminate on the basis of sex in educational programs or activities, including employment therein and admission to such programs and activities.</p>
	<p>Furthermore, it is the policy of RIT's administration that sexual harassment will not be condoned at this institution. The 1980 Final Interpretive Guidelines on Discrimination Because of Sex specify that harassment on the basis of sex is an unlawful employment practice under Section 703 of Title VII of the Civil Rights Act of 1964.</p>
	<p>In addition, the American With Disabilities Act of 1990 prohibits discrimination against any qualified individual with a disability in regard to all terms, conditions and privileges of employment who, with reasonable accommodation, can perform the essential job functions of such position.</p>
	<p>A student employee, who feels she/he has been a victim of discrimination or sexual harassment, may file a complaint through the SEO.</p>

