	<h2 id="4">4. The Federal Work-Study Program</h2>

	<p>The Federal Work-Study (FWS) Program refers to a form of federally funded campus-based student aid.</p>
	<p>Eligibility for campus-based financial aid, including FWS, is determined on the basis of a Free Application for Federal Student Aid (FAFSA), which is submitted annually before April 1st by the student to the RIT Financial Aid Office. The amount of the student's FWS award depends on the extent of his/her financial need. At RIT, the total cost of a FWS student's wages is borne by the employing department on its 71350 or 71450 (on-campus community service) budget line.</p>
	<p>FWS employment must be governed by employment conditions, including pay, that are reasonable according to the type of work performed, the geographic region, the employee's proficiency, and any applicable Federal, State, or Local laws.</p>
	<p>FWS employment must not displace employees (including those on strike) or impair existing service contracts. Also, if the employer has an employment agreement with an organization in the private sector, the organization's employees must not be replaced with FWS students. Replacement is interpreted as displacement.</p>

