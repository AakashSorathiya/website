	<h2 id="16">16. Compensation</h2>

	<p>RIT's student compensation policy has been developed with the following objectives in mind: equity among students performing comparable tasks, recognition for exceptional student employee performance, concern that student rates not impinge upon those of regular, full-time employees and ease and consistency in administrative maintenance.</p>
	<p>Each job is classified according to the On-Campus Job Classification section above. The classification level of a job determines its wage rate. A new job is always paid at the base rate of its level, with the exception of Level 5 positions.</p>
	<p>The student wage schedule is reviewed and if appropriate, will be revised. Changes in the <a href="/seo/assets/docs/pay-schedule.pdf">Student Wage Schedule</a> are implemented at the discretion of the Institute, and can be viewed on the SEO website.</p>
	<p>There are two merit levels built into each job level. Performance appraisals are no longer required for merit increases. Merit rates may be rewarded any time throughout the academic year; however, it is recommended that merit increases be given at the beginning of the quarter.</p>
	<p>Prior to the start of the student's employment, the supervisor should discuss the following items with the student:</p>

	<ul>
		<li><p>Specific job responsibility of the position.</p></li>
		<li><p>Standards of personal conduct on the job, including dress, personal calls, etc.</p></li>
		<li><p>Work schedule and relief/meal periods.</p></li>
		<li><p>Student employee's responsibility for equipment, supplies, security, record keeping, safety, etc.</p></li>
		<li><p>Penalty for unexcused absences or tardiness.</p></li>
	</ul>

	<p>If the student will be expected to take directions from personnel other than the hiring supervisor, these persons and their functions should be identified.</p>
	<p>In periods or situations when the twenty-hour rule is suspended, and the student works more than forty hours a week, the student must be paid time-and-a-half for hours in excess of forty. If the student is employed by more than one department, overtime costs are split between all departments. Therefore, it is essential that supervisors check with the student to ensure that the forty hour limit is not exceeded.</p>
	<p>Student employees are paid shift differential of $0.75 per hour only when assigned to "C" shift (12 Midnight - 8 AM) for exact hours worked. Student "C" shift overtime is $1.13 per hour.</p>
	<p>Student employees are paid only for hours worked. Therefore, they are not eligible for holiday pay or sick pay. If, however, a student is required to work on an Institute holiday, he/she will be paid time-and-a-half for the hours worked.</p>

