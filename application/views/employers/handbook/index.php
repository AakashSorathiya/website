	<h2 id="20">20. Grievance Resolution</h2>

	<p>Experience at RIT has shown that most problems between students and their supervisors can be solved with honest, non-confrontational discussion of the problem at hand. Since employment provides another aspect of the student's experiential learning, the attitudes and skills developed through this interpersonal process are significant elements in the student's maturation.</p>
	<p>All efforts on the part of a supervisor to correct or alleviate inappropriate behavior or inadequate performance should be documented. Such problems should be addressed on a direct, day-to-day basis. If, after a written warning and a specified time frame improvement is not evident, the student may be terminated "with cause".</p>
	<p>On the student employee's part, a serious attempt should be made to resolve the problem with his/her immediate supervisor. If the problem cannot be resolved at this level, the student should make an appointment with an SEO staff member to discuss the issue. SEO personnel will in turn contact the student's supervisor in an attempt to resolve the problem informally.</p>

