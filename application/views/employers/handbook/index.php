<div class="section">
	<h2 id="18">18. Benefits</h2>

	<p>Regular student employees are not eligible for sick pay, holiday pay, or unemployment benefits. Co-op students are eligible to receive two sick days per quarter and holiday pay.</p>
	<p>Worker's Compensation provides student employees with part of their average weekly wages, should they become injured on the job or in the course of employment, and as a result of the injury are prevented from working.</p>
	<p>Regardless of age and the number of hours worked, students do not meet ERISA (i.e. retirement) eligibility criteria, so long as their employment is incidental to their education.</p>
	<p>Students are also eligible for New York State statutory disability benefits should a non-occupational disability prevents them from working.</p>

