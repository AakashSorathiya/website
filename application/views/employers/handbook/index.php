	<h2 id="12">12. Employment</h2>

	<p>Student employees as defined in On-Campus Employment Eligibility fall into three categories: regular student employees (includes Federal Work-Study and non Work-Study student workers), on-campus co-op employees and graduate assistants. In all categories, student employees fall under jurisdiction of the Fair Labor Standards Act, and other relevant Federal and State Legislation.</p>
	<p>It is RIT's policy and practice that all regular student employees, both Federal Work-Study and non-Federal Work- Study, be hired according to the following procedure:</p>

	<ul>
		<li><p>All student job listings are listed with the SEO.</p></li>
		<li><p>Employers interview eligible candidates.</p></li>
		<li>
			<p>Once an offer of employment has been made:</p>
			<ul>
				<li><p><u>New student workers</u> must be given an 'Student Employment Verification Slip' to bring to the SEO in order to complete the employment verification process  and the web site information to complete Section 1 of the government mandated 1-9 Form- <a href="http://www.newi9.com">(http://www.newi9.com). The Employer number is 15083.</a></p></li>
				<li><p><u>Returning student workers</u> (students who have previously worked on-campus) must present Employment Eligibility Card, verified for the semester in which the student is to work.</p></li>
			</ul>
		</li>
		<li><p>After the student presents his/her Employment Eligibility Card to the employer, the employer may complete the Student Employment Hire Form and forward it to the Student Employment Office for processing.</p></li>
		<li><p>The appropriate NYS Pay Rate and Pay Day Form must also be included for all new student workers.</p></li>
	</ul>

	<p>Academic Direct Hire Positions are of an academic nature in which academic performance and background are the primary determinants of hiring (i.e. Printing Lab Assistants, Tutor/Note takers, etc.). Hiring procedures are the same as for regular student employees.</p>
	<p>Students may be rehired directly by a department in which they have previously been employed. Supervisors must ask to see the student's current Employment Eligibility Card. If the card is current, complete the Student Employment Hire Form and return it to SEO. It is essential that the student is a matriculated full-time student, taking a minimum of 12 credit hours per semester for undergraduate and 9 credit hours for graduate students. The SEO staff will check each rehire for employment eligibility. Supervisors will be notified by the SEO if a rehired student is not eligible for on-campus employment.</p>
	<p>Student employees may be terminated under any of the following conditions:</p>

	<ol>
		<li><p>End of job or project.</p></li>
		<li><p>Documented below standard job performance for which warnings and recommendations for improvement have previously been made.</p></li>
		<li><p>Change in a student's employment eligibility status as a full-time matriculated student (i.e. Dismissal for financial, social, or academic reasons (suspension), withdrawal from an approved full-time program of study, or graduation).</p></li>
		<li><p>International Student's I-20 or DS-2019 expiration.</p></li>
		<li><p>Theft and/or misuse of the Institute's equipment.</p></li>
		<li><p>On-the-job intoxication or drug use.</p></li>
		<li><p>Inappropriate on-the-job behavior.</p></li>
	</ol>

	<p>In terminations based on items 1 or 2, a two week notice of termination should be given by the student's supervisor. In terminations based upon changes in employment eligibility (item 3), or I-20 or DS-2019 expiration (item4), termination is immediate.</p>
	<p>In terminations arising from illegal behavior (items 5, 6 and 7), termination is immediate and may be followed by prosecution and/or requirements for restitution.</p>

