	<h2 id="19">19. On-the-Job Conduct</h2>

	<p>Consistent with the idea that a student's employment experience is an educational one and contributes to his/her development as an effective and productive citizen, emphasis on responsible conduct on the job is essential.</p>
	<p>RIT expects its student employees, as responsible members of society, to comply with all federal, state and local laws affecting them as employees or as private citizens while working for the Institute on or off its property. RIT will cooperate with all legally constituted law-enforcement agencies with respect to student employees accused of committing an act which may be in violation of the law.</p>
	<p>Consumption of any alcoholic beverages by student employees during their working hours on their scheduled work days, on or off-campus, is not permitted. The use or possession of controlled substances is not permitted unless prescribed by a physician. Reporting to work in an inebriated condition or under the influence of non-medically prescribed controlled substances is also prohibited.</p>
	<p>No property is to be removed from the RIT campus without prior written approval from the Director of Facilities Management Services. Theft of tangible items or of computer time or misuse of telephones (i.e. long distance personal calls), equipment, or facilities available to students during their working hours are grounds for <b>immediate</b> termination.</p>
	<p>Upon separation from RIT, debts to the Institute incurred by the student employee are immediately due and payable in full.</p>
	<p>In instances where a student employee's job performance does not meet the standard of competence specified in his/her job description, or in instances where on-the-job conduct is in violation of generally agreed upon standards of behavior (i.e. punctuality, courtesy, cleanliness, accountability, etc.) the supervisor should:</p>

	<ul>
		<li><p>Discuss with the student employee the responsibilities of the job and the expected standards of performance, give direction for improvement and indicate a time-frame, (i.e. one week) within which improvement is to occur.</p></li>
		<li><p>If the unsatisfactory performance continues, a written warning should be given specifying the problem, the time limit for the changes in performance to be accomplished and a clear indication that failure to conform to this directive will result in immediate dismissal.</p></li>
		<li><p>If the conditions listed in #2 are not met, the student may be dismissed.</p></li>
	</ul>

	<p>Supervisors are counseled to keep written records of all disciplinary encounters or actions involving student employees. The Student Employment staff is always willing to discuss employment problems and to assist in effective problem resolution.</p>

