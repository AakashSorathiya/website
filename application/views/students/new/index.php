<h1>New Student Information</h1>

<div class="clearfix">
	<img class="img-polaroid pull-right" src="./assets/img/live/student.jpg" />

	<p>New students who wish to work on-campus must first receive an offer of employment from an on-campus employer. At this time, your new supervisor will complete a 'Student Employment Verification Slip' for you to bring to the Student Employment Office so you will be able to get your Employment Eligibility Card. You will be required to complete the government mandated I-9 Form and submit original documentation to prove identity and employment eligibility. This process must be completed in person in the Student Employment Office (SEO). The SEO is located in the University Services Center in Room 1350. Office hours are Monday - Friday, 8:30am - 4:30pm.</p>
	<p>In order to work on-campus, students must:</p>

	<ol>
		<li><p>Be registered with full-time status (minimum of 12 credit hours).</p></li>
		<li><p>Provide proof of on-campus employment, supplied by your employer.  If you need to find a job on campus, check out our <a href="?/home/job/">Getting a Job</a> page for more information about how to find a job on-campus.</p></li>
		<li><p>Follow these <a target="_blank" href="http://www.rit.edu/emcs/seo/assets/docs/i-9-acceptable-documents.pdf">instructions</a> to complete the government mandated I-9 Form.
				You will be required to show original documentation to verify your identity and eligibility to work.</p>
			<p><b>Note:</b> The I-9 process has gone electronic.  Please see the <a href="?/home/news">Announcements</a> page for additional information regarding the new electronic I-9.</p>
			<p>
				<b>ALL DOCUMENTS MUST BE ORIGINAL COPIES.</b><br>
				<b>PHOTOCOPIES WILL NOT BE ACCEPTED.</b>
			</p>
		</li>
		<li><p>Pick up a Student Employment Office Eligibility Card from the office located at the University Services Center in room 1350.</p></li>
	</ol>
</div>