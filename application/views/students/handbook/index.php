<p><b><a name="3"></a>3. The Federal Work-Study Program</b></p>
<p>
	The Federal Work-Study (FWS) Program is a federally funded program administered by RIT to promote
	access to employment. This program assists students who demonstrate financial need as defined by
	the Federal Government. The Financial Aid Office selects on the basis of financial need as many
	participants for this program as funding allows.
</p>
<p>
	Earnings during the academic year vary and are dependent upon maximum earnings level and the number
	of hours actually worked. Each student is paid bi-weekly by check or direct deposit for the number
	of hours worked. FWS earnings are considered income, and subject state and federal taxes. Please
	refer to the <a href="#12">Tax Guidelines</a> section for more information.
</p>
<p>
	Financial need is defined as the difference between the total cost of a student's educational expenses
	and the amount of money the student and his or her parents are expected to contribute to those costs.
	To be eligible to receive a FWS award, a student must meet the following criteria:
</p>
<ul>
	<li>Be in need of the earnings from such employment in order to pursue a course of study.</li>
	<li>Be capable of maintaining a good standing in such course of study while employed.</li>
	<li>Be a matriculated student.</li>
	<li>Be a citizen of the United States or an eligible non-resident.</li>
</ul>
<p>
	Students are expected to apply FWS earnings toward educational expenses at RIT. FWS earnings are not
	automatically applied toward tuition. FWS awards that are not used cannot be carried over to the next
	academic year. Any money that is not earned from the award is considered unrecoverable. The amount
	unearned is not credited toward your tuition bill. Students who have reached their award limits are
	advised to go to the Financial Aid Office to inquire as to whether their award can be increased. FWS
	is not automatically renewed. Students must reapply by the deadline of April 1st of each year.
</p>
