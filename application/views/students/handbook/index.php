	<h2 id="12">12. Tax Guidelines</h2>

	<p>Earnings, including FWS earnings, are subject to all federal, state and local income taxes. All students must file a <a href="http://www.irs.gov/pub/irs-pdf/fw4.pdf">Form W-4</a> and <a href="http://www.tax.ny.gov/pdf/2011/fillin/wt/it2104_611_fill_in.pdf">IT-2104</a> for tax purposes when they begin to work at RIT. If you are a nonresident alien (NRA) student, you may be subject to special withholding requirements; please do not submit these forms and contact Keithaleen Jones in the Payroll Department - <a href="mailto:ktjpay@rit.edu">ktjpay@rit.edu</a>.</p>
	<p><i>* In the event that you do not submit either form, tax withholding will occur at the single with zero exemptions level (does not include NRA).</i></p>
	<br>

	<p>Students are not eligible to claim "exempt" on the Form W-4 unless all of the following apply:</p>
	<ul>
		<li>
			<p>The student's income exceeds $1000 and his income includes more than $350 of unearned income (for example, interest and dividends)</p>
			<p><b>AND</b></p>
		</li>
		<li>
			<p>The student can be claimed by another person as a dependent on their tax return</p>
			<p><b>AND</b></p>
		</li>
		<li>
			<p>Last year the student had a right to a refund of all federal income tax withheld due to no tax liability</p>
			<p><b>AND</b></p>
		</li>
		<li><p>This year the student expects a refund of all federal income tax withheld because the student expects to have no tax liability.</p></li>
	</ul>
	<br>

	<p>Students are not eligible to claim "exempt" on the IT-2104 unless the following conditions are met:</p>
	<ul>
		<li>
			<p>Student must be under age 18, or over age 65, or a full-time student under age 25</p>
			<p><b>AND</b></p>
		</li>
		<li>
			<p>Student did not have a New York income tax liability for the previous year.</p>
			<p><b>AND</b></p>
		</li>
		<li>
			<p>Student does not expect to have a New York income tax liability for the current year.</p>
			<p><b>OR</b></p>
		</li>
		<li><p>Student meets the conditions set forth under the Service Members Civil Relief Act, as amended by the Military Spouses Residency Relief Act.</p></li>
	</ul>

