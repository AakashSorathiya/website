<p><b><a name="5"></a>5. Qualifying for On-Campus Employment</b></p>
<p>
	For on-campus student employment, you are required to obtain a Student Employment Eligibility Card from the
	Student Employment Office. To do so, you must first have an offer of employment and bring the Student 
	Employment Verification Form to the Student Employment Office which your supervisor has completed for you.
	You must also be a full-time matriculated RIT student, AND be registered for a minimum of 12 credit hours
	for the quarter(s) you wish to work. You will be required to complete the government mandated I-9 Form to
	prove your employment eligibility by submitting appropriate documentation to the SEO staff. 
	<a href="/seo/assets/docs/i-9-acceptable-documents.pdf">Required I-9 Form documentation</a>
</p>
<p>
	The SEO also functions as a brokerage service for RIT students seeking off-campus employment. To apply
	for off-campus jobs you must first check the <a href="?/students/offcampus/">Off-Campus Job Postings</a>
	located on the SEO website. Directions for application are found on each job listing. It is the student's
	responsibility to contact the employer to schedule an interview. The employer will make the hiring
	decision. If at any time you encounter problems with an off-campus employer, you are advised to contact
	the SEO immediately at 585-475-2631.
</p>
<p>
	If you are an International student, please refer to the section on Employment Eligibility for International
	Students.
