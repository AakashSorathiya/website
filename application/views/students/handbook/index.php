<p><b><a name="18"></a>18. Job Related Problems</b></p>
<p>
	Students should make a serious attempt to resolve any on the job problems with supervisors or peers.
	If the problem cannot be resolved at this level, then an appointment should be made with a staff member
	of Student Employment. Student Employment will in turn, contact the supervisor or peer in a final attempt
	to resolve the problem informally. If informal attempts to resolve the problem are unsatisfactory, a
	formal grievance procedure is available. Additional information can be found at <a href="http://finweb.rit.edu/humanresources/policies/unethicalconduct.html" target="_blank">University Resources for Discussing and/or Reporting Concerns about Unethical Conduct, Harassment, or Discrimination</a>.
</p>
<p>
	Students, who have completed the employment eligibility process and have worked for an on-campus employer
	for three weeks or more without receiving a paycheck, should check with their supervisor as soon as the
	first expected paycheck is not received. Often times the problem can be rectified immediately and
	arrangements can be made for the student to receive partial payment of wages owed. If the supervisor cannot
	assist the student with the problem, then the student is advised to contact the Student Employment Office.
</p>
<p>
	Remember, the benefits of working extend far beyond the obvious financial ones. Getting a job and working
	to the best of your ability will provide valuable learning experience not otherwise available. Your job
	may or may not be career related, but you will have opportunities for growth and success!
</p>
