	<h2 id="6">6. International Students - Employment Eligibility</h2>

	<p>To be eligible for on-campus employment and before a Student Employment Eligibility Card can be obtained, international students must first:</p>
	<ul>
		<li><p>Have an offer of employment from an RIT Employer.</p></li>
		<li><p>Be enrolled with a minimum of 12 credit hours as an undergraduate student or 9 credit hours as a graduate student, or have a full-time equivalency for all semesters of employment.</p></li>
		<li><p>Have a valid F1 or J1 Visa and/or I-94.</p></li>
	</ul>
	<p>International students are allowed to work up to 20 hours per week during academic periods, and up to 40 hours per week during breaks, summer and intersession.</p>
	<br>

	<p>International students seeking employment who <u>DO NOT</u> already have a Social Security Card must have a job or job offer BEFORE he or she can apply for a Social Security number. International students must follow the procedures listed below:</p>
	<ul>
		<li><p>Search for employment via the SEO website or by visiting employing departments through the campus.</p></li>
		<li><p>Once you have an offer of employment and the Employment Verification Form provided to you by your supervisor, you are required to obtain an Employment Eligibility Card from the Student Employment Office. (See Qualifying for On-Campus Employment) Be sure to bring your I-20 or DS-2019 with passport, I-94, and visa to the SEO.</p></li>
		<li><p>After getting a job, ask your employer for a copy of your Student Employment Hire Form and take the form to the ISSO.  <b>Be sure the hire form has your employer’s original signature.</b></p></li>
		<li><p>The ISSO will provide you with a letter of verification for the Social Security Administration.</p></li>
		<li><p>You are now eligible to apply for your Social Security Number. The ISSO will provide you with additional information on obtaining your Social Security Card.</p></li>
	</ul>
	<p>F1 and J1 Visa Status:</p>
	<p>International students are responsible for keeping their status up-to-date. Students must notify the SEO whenever their I-20 (for F1 Visas) or DS-2019 (for J1 Visas) is updated. <u><b>Students with expired status are not eligible to work.</b></u> Thirty days prior to a student's I-20 or DS-2019 status expiration date, the student's employers will receive an e-mail alert advising them that a student's status is about to expire. At the end of the 30 days, if the student has not updated his/her visa records in the SEO, the student will be terminated from employment.</p>

