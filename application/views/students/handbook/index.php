<p><b><a name="6"></a>6. International Students - Employment Eligibility</b></p>
<p>
	To be eligible for on-campus employment and before a Student Employment Eligibility Card can be obtained,
	international students must first:
</p>
<ul>
	<li>Have an offer of employment from an RIT Employer</li>
	<li>
		Be enrolled with a minimum of 12 credit hours, or have a full-time equivalency for the quarter(s)
		of employment.
	</li>
	<li>Have a valid F1 or J1 Visa.</li>
</ul>
<p>
	International students are allowed to work up to 20 hours per week during academic quarters, and up to
	40 hours per week during breaks and approved vacation quarters. Due to the nature of the International
	Student Program, International Students are entitled to one vacation quarter per academic year, upon
	approval by the International Student Services Office (ISSO). During the approved vacation quarter, 
	International students amy work up to 25 hours per week during the academic quarter and up to 40 hours
	per week during break periods.<br><br>
	International students seeking employment who <u>DO NOT</u> already have a Social Security Card
	must have a job or job offer BEFORE he or she can apply for a Social Security number. International students 
	must follow the procedures listed below:
</p>
<ul>
	<li>
		Search for employment via the SEO website or by visiting employing departments through the campus.
	</li>
	<li>
		Once you have an offer of employment and the Employment Verification Form provided to you by your supervisor,
		you are required to obtain an Employment Eligibility Card from the Student Employment Office. Be sure to bring 
		your I-20 or DS-2019 with passport and visa to the SEO.</li>
	<li>
		After getting a job, ask your employer for a copy of your Student Employment Hire Form and take the
		form to the ISSO.
	</li>
	<li>
		The ISSO will provide you with a letter of verification for the Social Security Administration.
	</li>
	<li>
		You are now eligible to apply for your Social Security Number. The ISSO will provide you with
		additional information on obtaining your Social Security Card.
	</li>
</ul>
<p>
	F1 and J1 Visa Status:
</p>
<p>
	International students are responsible for keeping their status up-to-date. Students must notify the SEO
	whenever their I-20 (for F1 Visas) or DS-2019 (for J1 Visas) are updated. <u>Students with expired status are
	not eligible to work.</u> Thirty days prior to a student's I-20 or DS-2019 status expiration date, the student's 
	employers will receive an e-mail alert advising them that a student's status is about to expire. At the end of the 30 days,
	if the student has not updated his/her visa records in the SEO, the student will be terminated from employment.
</p>
