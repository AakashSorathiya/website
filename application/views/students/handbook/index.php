<p><b><a name="13"></a>13. Social Security And Medicare Taxes (FICA)</b></p>
<p>
	Full-time, matriculated students are exempt from paying Social Security Tax (FICA) if they meet the
	following criteria:
</p>
<ul>
	<li>Student is registered for a minimum of six credit hours per quarter.</li>
	<li>Student is physically working on the RIT campus.</li>
</ul>
<p>
	Students who are registered for less than six credit hours or are paid by RIT, but working at an
	off-campus location, will have FICA taxes deducted from their paychecks at a rate of 6.2% with
	RIT paying the matching share. Therefore, it is extremely important that the employer indicate on
	the SEO Hire Form if the student is working off-campus.
</p>
<p>
	Foreign students with a valid I-20 or DS-2019 are exempt from this policy.
</p>
