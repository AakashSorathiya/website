<p><b><a name="16"></a>16. Job Benefits</b></p>
<p>
	RIT student employees are covered by New York State's Workers' Compensation if injuries are incurred
	while on the job. If students are unable to continue to work due to an illness or an injury sustained
	other than in the course of employment, the student will be eligible for New York State statutory
	disability benefits. In either case, contact your supervisor immediately for instructions. Students
	are not eligible for sick pay, holiday pay, or unemployment insurance benefits. However, if required
	to work on an Institute Holiday, students will be paid time-and-a-half for hours worked.
</p>
