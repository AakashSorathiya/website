	<h2 id="13">13. Social Security and Medicare Taxes (FICA)</h2>

	<p>Full-time, matriculated students are exempt from paying Social Security Tax (FICA) if they meet the following criteria:</p>

	<ul>
		<li><p>Student is registered for a minimum of six credit hours per semester.</p></li>
		<li><p>Student is physically working on the RIT campus.</p></li>
	</ul>

	<p>Students who are registered for less than six credit hours or are paid by RIT, but working at an off-campus location, will have FICA taxes deducted from their paychecks at a rate of 7.65% with RIT paying the matching share. Therefore, it is extremely important that the employer indicate on the SEO Hire Form if the student is working off-campus.</p>
	<p>Foreign students with a valid I-20 or DS-2019 are exempt from this policy.</p>

