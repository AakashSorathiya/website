<p><b><a name="12"></a>12. Tax Guidelines</b></p>
<p>
	Earnings, including FWS earnings, are subject to all federal, state and local income taxes. All students must file a 
	<a href="http://www.irs.gov/pub/irs-pdf/fw4.pdf">Form W-4</a> 
	and
	<a href="http://www.tax.ny.gov/pdf/2011/fillin/wt/it2104_611_fill_in.pdf">IT-2104</a>
	for tax purposes when they begin to work at RIT. If you are a nonresident alien (NRA) student, you may be subject to
	special withholding requirements; please <u>do not</u> submit these forms and contact Keithaleen Jones in the Payroll Department -
	<a href="mailto:ktjpay@rit.edu">ktjpay@rit.edu</a>. 
	<i>*In the event that you do not submit either form, tax withholding will occur 
	at the single with zero exemptions level (does not include NRA).</i>
	<br>
	<br>
	Students are <u>not eligible</u> to claim "exempt" on the Form W-4 unless all of the following apply:
</p>
<ul>
	<li>
		The student's income exceeds $950 and his income includes more than $300 of unearned income (for example,
		interest and dividends) AND;
	</li>
	<li>
		The student can be claimed by another person as a dependent on their tax return AND;
	</li>
	<li>
		Last year the student had a right to a refund of all federal income tax withheld due to no tax liability AND;
	</li>
	<li>
		This year the student expects a refund of all federal income tax withheld because the student expects to have no tax
		liability.
	</li>
</ul>
<p>
	Students are <u>not eligible</u> to claim "exempt" on the IT-2104 unless the following conditions are met:
</p>
<ul>
	<li>
		Student must be under age 18, or over age 65, or a full-time student under age 25 AND;
	</li>
	<li>
		Student did not have a New York income tax liability for 2010 AND;
	</li>
	<li>
		Student does not expect to have a New York income tax liability for 2011.
	</li>
</ul>
<p>
	OR
</p>
<ul>
	<li>
		Student meets the conditions set forth under the Servicemembers Civil Relief Act, as amended by the Military
		Spouses Residency Relief Act.
	</li>		
</ul>

