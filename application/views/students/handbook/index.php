<p><b><a name="10"></a>10. On-Campus Job Classification and Work Hours</b></p>
<p>
	On-Campus student jobs are classified according to the skills and experience required, going from
	Level 2 to Level 5, with Level 5 being the highest.
</p>
<ul>
	<li>
		<b>Level 2: </b>Semi-skilled or positions with some degree of specialized knowledge or training
		requiring little or no experience and are routine and well-supervised. Examples of these types of
		positions are file clerks, food service workers, campus tour guides, office assistants, and data
		entry clerks.
	</li>
	<li>
		<b>Level 3: </b>Specialized knowledge and training requiring at least one year of experience,
		independent judgment, and at times, supervisory responsibility such as parking attendants,
		student ambassadors, computer operators, and athletic trainers.
	</li>
	<li>
		<b>Level 4: </b>Two or three years of specialized course work, specialized training, experience,
		and a high degree of independent judgment. These positions may also involve supervision of others.
		Examples of these types jobs are tutors/note-takers, graders, and lab assistants.
	</li>
	<li>
		<b>Level 5: </b>Designates a category of jobs, with wages being determined individually based on
		comparable or identical full-time positions. The skill requirements and employee responsibilities
		of such positions are identical for student employees and full-time employees. These jobs may also
		include those positions in which a student has worked during the previous quarter in an on-campus
		co-op position, and is continuing in the position as a regular student employee. These jobs also
		require a highly specialized skill and/or several years of previous experience. Examples of Level 5
		jobs are web developers, research assistants, computer programmers, and supervisor positions.
	</li>
</ul>
<p>
	The average student works 8 to 12 hours a week. While classes are in session, students may work up to
	a maximum of 20 hours per week, if the job permits. When classes are not in session, students may be
	able to work up to 40 hours per week. The number of hours per week and actual hours to be worked will
	be determined by the employer. Most on-campus employers will work around the student's class schedule.
</p>
