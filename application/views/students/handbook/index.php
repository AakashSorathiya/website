<p><b><a name="17"></a>17. Standards of Employment and Student Conduct</b></p>
<p>
	Student employees are representatives of RIT and are expected to act in a manner consistent with the
	mission and goals of RIT and their department of employment. RIT student employees are expected to be
	punctual, reliable, dress appropriately and perform work in a satisfactory manner. Students are also
	expected to comply with all federal, state and local laws while working for the Institute on or off
	its property. Working under the influence of alcohol or controlled substances is not permitted. Theft
	of tangible items or computer time, or misuse of telephones (i.e. unauthorized long distance personal
	calls), equipment or facilities available to students during their work hours is grounds for immediate
	dismissal and possible prosecution. As student employees are paid for actual hours worked, any student
	found adding unauthorized work hours will be immediately terminated from employment and may face
	criminal charges.
</p>
