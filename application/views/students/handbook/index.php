<p><b><a name="14"></a>14. On-Campus Co-op Employment</b></p>
<p>
	Co-op employment is a mandatory component of many RIT degree programs. It is essential that
	on-campus co-op employment meet the criteria of program validity inherent in the Institute's
	commitment to quality experiential learning.
</p>
<p>
	 It is RIT policy and practice that all on-campus co-op students be hired according to the
	 following procedure:
</p>
<ul>
	<li>
		On-campus co-op positions are listed with the Center for Cooperative Education and Career Services.
	</li>
	<li>Students seeking on-campus co-op respond directly to the departments listing the positions.</li>
	<li>RIT Student Hire Forms are to be completed and returned to the SEO for processing.</li>
	<li>
		The student must have a completed I-9 Form on file in the SEO to be eligible for work, and must
		also be registered for co-op.
	</li>
</ul>
<p>
	The SEO will no longer publish the annual Co-op Wage Schedule. Employers may use their own discretion
	when assigning co-op wages. For assistance, employers may contact the Co-op Office at 475-2301 to request
	a copy of their most recent Co-op Wage Survey.
</p>
<p>
	Unlike regular student employees, on-campus co-op students are also eligible for holiday pay for all
	Institute holidays falling within a given co-op employment quarter. They are also eligible for sick pay
	on the ratio of two days per quarter worked. Sick day benefits are cumulative to a maximum of six paid
	sick days. On-campus co-op students are not eligible for Unemployment Insurance Benefits. On-campus co-op
	employees are exempt from FICA withholding as long as they are registered for co-op.
</p>
