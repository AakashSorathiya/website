<p><b><a name="8"></a>8. When to Begin Employment</b></p>
<p>
	Students do not necessarily need to start employment when they come on-campus. During the academic year,
	students may choose, for example, to begin work in January rather than September. However, there are a
	number of factors to consider before deferring employment. First, students may not be able to work in
	certain departments that may initiate most of their hiring in the Fall Quarter. In addition, starting
	later in the year may not allow students with FWS awards to reach the maximum earnings level of their
	award amount.
</p>
<p>
	When students will work is dependent upon choice of jobs and willingness/ability to work during the
	summer. Students may work part-time during the academic year and full-time during June, July and August.
	During summer sessions, however, all students are employed through institutional funding. Students may
	also be able to work full-time when classes are not in session during break periods throughout the
	academic year.
