<div id="1" class="section">
	<b>What do I need for a Student Employment Office Eligibility Card?</b>
	<ul>
		<li><p>You must be registered as a full time student with a minimum of 12 credit hours.</p></li>
		<li><p>You will need to bring proof of employment with you to verify that you have a job on campus.</p></li>
		<li><p>A valid form of identification.  The lists can be found <a href="/seo/assets/docs/i-9-acceptable-documents.pdf" target="_blank">here</a>.  You must provide at least one item from list A or an item from both lists B and C.</p></li>
		<li><p>Your student identification number.</p></li>
	</ul>
</div>

