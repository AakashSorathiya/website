<div id="4" class="section">
	<b>What is Federal Work Study?</b>
	<p>The Federal Work Study (FWS) Program provides funds for part-time employment to help needy students to finance the costs of postsecondary education. Students can receive FWS funds at approximately 3,400 participating postsecondary institutions. Hourly wages must not be less than the federal minimum wage.</p>
</div>

<div id="5" class="section">
	<b>What qualifies a job as a Federal Work Study?</b>
	<p>Any position offered on RIT's campus is a Federal Work Study job (with a few exceptions).</p>
</div>

<div id="6" class="section">
	<b>Are there any jobs on campus that do not qualify for Federal Work Study?</b>
	<p>Most positions off campus do not qualify for federal work study.  There are a couple positions on campus that do not qualify such as Java's in the Wallace Library and Barnes and Noble at Park Point.</p>
</div>