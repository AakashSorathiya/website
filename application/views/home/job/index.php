	<p>There are many ways you can get a job:</p>
	<ul>
		<li>
			Job Postings
			<p>Refer to <a href="?/students/oncampus/">On-Campus Job Postings</a> or <a href="?/students/offcampus/">Off-Campus Job Postings</a> on our SEO Website - we update them daily.  Fall quarter positions are normally posted around mid-August.  Follow the instructions for each posting as to how to apply.</p>
		</li>
		<li>
			Networking
			<p>Ask around in the department you are majoring in such as your academic advisor, professors, departmental staff - not all departments post with SEO.</p>
			<p>Several departments have openings fairly often such as Food Service, Wallace Library, NTID Notetaking, or Maintenance - go to the department or restaurant/cafeteria and ask for the supervisor.  Remember to bring a resume!</p>
		</li>
	</ul>
